#include <iostream>
using namespace std;

vector<int> findSlidingWindow(vector<int> &nums, int k)
{
    int n = nums.size();
    vector<int> max_elements;
    deque<int> dq;
    for (int i = 0; i < n; i++)
    {

        if (!dq.empty() && (dq.front() <= i - k))
        {
            dq.pop_front();
        }
        while (!dq.empty() && nums[dq.back()] <= nums[i])
        {
            dq.pop_back();
        }
        dq.push_back(i);
        if (i >= k - 1)
        {
            max_elements.push_back(nums[dq.front()]);
        }
    }
    return max_elements;
}

class Solution
{
public:
    vector<int> maxSlidingWindow(vector<int> &nums, int k)
    {
        return findSlidingWindow(nums, k);
    }
};